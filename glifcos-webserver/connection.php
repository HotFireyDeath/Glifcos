if (isset($_GET["type"])){
    if ($_GET["type"] === "ping"){
        // send either "apple" or "grape"
        // apple = active, grape = error.
        echo "apple";
    }
    elseif ($_GET["type"] === "grudgesync"){
        $data = json_decode($_GET["grudge"], true);
        if (is_file("grudge.json")){
            unlink("grudge.json");
        }
        $handle = fopen("grudge.json", "w+");
        fwrite($handle, $data);
        fclose($handle);
    }
}
else{
    die("Invalid connection.");
}